package com.example.apparelproject.model;

public class Transaction {
    private String gambar, nama, warna, asalProduct, ketBayar, tanggalTransaksi, ketSampai, tanggalSampai, ukuran, jumlah, harga, totalHarga;

    public String getGambar() {
        return gambar;
    }

    public void setGambar(String gambar) {
        this.gambar = gambar;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getWarna() {
        return warna;
    }

    public void setWarna(String warna) {
        this.warna = warna;
    }

    public String getAsalProduct() {
        return asalProduct;
    }

    public void setAsalProduct(String asalProduct) {
        this.asalProduct = asalProduct;
    }

    public String getKetBayar() {
        return ketBayar;
    }

    public void setKetBayar(String ketBayar) {
        this.ketBayar = ketBayar;
    }

    public String getTanggalTransaksi() {
        return tanggalTransaksi;
    }

    public void setTanggalTransaksi(String tanggalTransaksi) {
        this.tanggalTransaksi = tanggalTransaksi;
    }

    public String getKetSampai() {
        return ketSampai;
    }

    public void setKetSampai(String ketSampai) {
        this.ketSampai = ketSampai;
    }

    public String getTanggalSampai() {
        return tanggalSampai;
    }

    public void setTanggalSampai(String tanggalSampai) {
        this.tanggalSampai = tanggalSampai;
    }

    public String getUkuran() {
        return ukuran;
    }

    public void setUkuran(String ukuran) {
        this.ukuran = ukuran;
    }

    public String getJumlah() {
        return jumlah;
    }

    public void setJumlah(String jumlah) {
        this.jumlah = jumlah;
    }

    public String getHarga() {
        return harga;
    }

    public void setHarga(String harga) {
        this.harga = harga;
    }

    public String getTotalHarga() {
        return totalHarga;
    }

    public void setTotalHarga(String totalHarga) {
        this.totalHarga = totalHarga;
    }
}
